marks = int(input("Enter your marks : "))

if marks >= 90:
    print("You grade is Ex")
elif marks >= 80:
    print("You grade is A")
elif marks > 70:
    print("Your grade is B")
elif marks >= 60:
    print("You grade is C")
elif marks >= 50:
    print("You grade is D")
else:
    print("You grade is F")


